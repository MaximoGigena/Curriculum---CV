<script setup>
import { ref, reactive } from 'vue'
import emailjs from '@emailjs/browser'
import RedesSociales from './RedesSociales.vue'

// Configuración de redes sociales
const misRedes = {
  linkedin: 'https://linkedin.com/in/tu-usuario',
  github: 'https://github.com/tu-usuario',
  twitter: 'https://twitter.com/tu-usuario',
  instagram: 'https://instagram.com/tu-usuario'
}

// Estado del formulario
const form = reactive({
  nombre: '',
  email: '',
  asunto: '',
  mensaje: '',
  privacidad: false
})

const enviando = ref(false)
const mensajeEstado = ref(null)
const avionVolar = ref(false)

const enviarFormulario = async () => {
  enviando.value = true
  avionVolar.value = true
  
  try {
    const templateParams = {
      name: form.nombre,
      from_name: form.nombre,
      from_email: form.email,
      subject: form.asunto,
      message: form.mensaje,
      to_email: 'gigenamaximo@gmail.com'
    }
    
    await emailjs.send(
      'service_b6yktyk',
      'template_u86euon',
      templateParams,
      'XZ83wdYR-p5grqUSC'
    )
    
    mensajeEstado.value = {
      tipo: 'exito',
      texto: '¡Mensaje enviado con éxito! Te responderé pronto.'
    }
    
    // Resetear formulario
    Object.keys(form).forEach(key => {
      if (key !== 'privacidad') form[key] = ''
    })
    form.privacidad = false
    
    setTimeout(() => {
      mensajeEstado.value = null
      avionVolar.value = false
    }, 3000)
    
  } catch (error) {
    console.error('Error al enviar:', error)
    mensajeEstado.value = {
      tipo: 'error',
      texto: 'Error al enviar. Por favor, intenta de nuevo.'
    }
    
    setTimeout(() => {
      mensajeEstado.value = null
      avionVolar.value = false
    }, 5000)
  } finally {
    enviando.value = false
  }
}
</script>

